func TestPrvKey(t *testing.T) {

	prv_1 = PrivateKeyFromSeed(seed_1)
	if testing.Verbose() {
		fmt.Printf("PRIVATE (%s)\n", hex.EncodeToString(seed_1))
		fmt.Printf("        d = %s\n", hex.EncodeToString(prv_1.D().Bytes()))
		fmt.Printf("        '%s'\n", util.EncodeBinaryToString(seed_1))
	}

	pub_1 = prv_1.Public()
	pubB := pub_1.Bytes()
	if testing.Verbose() {
		fmt.Printf("PUBLIC  = %s\n", hex.EncodeToString(pubB))
		fmt.Printf("        = '%s'\n", util.EncodeBinaryToString(pubB))
	}

	if !bytes.Equal(pubB, q_1) {
		fmt.Printf("PUBLIC_GOT = %s\n", hex.EncodeToString(pubB))
		fmt.Printf("PUBLIC_EXP = %s\n", hex.EncodeToString(q_1))
		t.Fatal("Public key mismatch")
	}
}

func TestSign(t *testing.T) {
	sigT, err := prv_1.Sign(data_1)
	if err != nil {
		t.Fatal(err)
	}
	sigX := sigT.Bytes()
	if testing.Verbose() {
		fmt.Printf("SIG=%s\n", hex.EncodeToString(sigX))
	}
	if bytes.Compare(sigX, sig_1) != 0 {
		t.Logf("SIG! = %s\n", hex.EncodeToString(sig_1))
		t.Fatal("Signature mismatch")
	}
	if !pub_1.Verify(data_1, sigT) {
		t.Fatal("Verify failed")
	}
}

func TestMult(t *testing.T) {
	prv_1 = PrivateKeyFromD(new(big.Int).SetBytes(d_1))
	pub_1 = prv_1.Public()
	prv_2 = PrivateKeyFromD(new(big.Int).SetBytes(d_2))
	pub_2 = prv_2.Public()

	s1 := pub_1.Mult(prv_2.D()).AffineX().Bytes()
	s2 := pub_2.Mult(prv_1.D()).AffineX().Bytes()
	if testing.Verbose() {
		fmt.Printf("S1 = %s\n", hex.EncodeToString(s1))
		fmt.Printf("S2 = %s\n", hex.EncodeToString(s2))
	}

	if bytes.Compare(s1, ss_1) != 0 {
		t.Fatal("Wrong shared secret")
	}
	if bytes.Compare(s1, s2) != 0 {
		t.Fatal("Shared secret mismatch")
	}
}
